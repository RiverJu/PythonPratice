def add(num1, num2): #add함수의 정의
    return num1 + num2

def min(num1, num2): #min 함수의 정의
    return num1 - num2

def mul(num1, num2): #mul 함수의 정의
    return num1*num2

def div(num1, num2): #div 함수의 정의
    return num1/num2

def main(): #프로그램의 실행 흐름을 담고 있는 main함수의 정의
    print(add(5,3))
    print(min(5,3))
    print(mul(5,3))
    print(div(5,3))

main()
